# File: simple_interest.py

# Define the variables
principal = 1000  # Initial investment of $1000
rate = 0.05       # 5% annual interest rate
time = 3          # Investment period of 3 years

# Calculate simple interest using the formula I = P * R * T
interest = principal * rate * time

# Print the calculated interest in a human-readable format
print(f"The simple interest is: {interest}.")

